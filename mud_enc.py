# ============ رسم الصناديق (إطارات القوائم والبانر) ============
def ارسم_صندوق(الأسطر, اعدادات_المظهر, الاعراض=None, الاتجاه="rjust", حشو=2, الوان_مخصصة=None):
    """
    الأسطر: قائمة نصوص. السطر "---" يُرسم كفاصل ╠══╣
    الاعراض: قائمة أعراض مطابقة لعدد الأسطر (تحكم منفصل بعرض كل سطر)، افتراضياً تلقائي
    الاتجاه: نص واحد يطبق على الكل (rjust/ljust/center) أو قائمة اتجاهات لكل سطر على حدة
    الوان_مخصصة: قائمة اختيارية بطول الأسطر، كل عنصر إما كود لون ANSI يُطبق على ذاك
                 السطر بمفرده (بعد التهيئة، لتفادي كسر bidi)، أو None لاستخدام لون النص الافتراضي
    """
    لون_اطار = اعدادات_المظهر.get("اطار", ل.سماوي)
    لون_خلفية = اعدادات_المظهر.get("خلفية") or ""
    لون_نص = اعدادات_المظهر.get("نص", ل.أبيض_ف)

    اطوال_خام = [0 if س == "---" else len(س) for س in الأسطر]
    اقصى_عرض = max(اطوال_خام) if اطوال_خام else 0

    if الاعراض is None:
        الاعراض = [اقصى_عرض] * len(الأسطر)

    if isinstance(الاتجاه, str):
        الاتجاهات = [الاتجاه] * len(الأسطر)
    else:
        الاتجاهات = الاتجاه

    عرض_الصندوق = max(الاعراض) + حشو * 2 if الاعراض else اقصى_عرض + حشو * 2

    خط_علوي = لون_اطار + لون_خلفية + "╔" + "═" * عرض_الصندوق + "╗" + ل.إعادة
    خط_سفلي = لون_اطار + لون_خلفية + "╚" + "═" * عرض_الصندوق + "╝" + ل.إعادة
    خط_فاصل = لون_اطار + لون_خلفية + "╠" + "═" * عرض_الصندوق + "╣" + ل.إعادة

    النتيجة = [خط_علوي]
    for i, س in enumerate(الأسطر):
        if س == "---":
            النتيجة.append(خط_فاصل)
            continue

        عرض_السطر = الاعراض[i]
        اتجاه_السطر = الاتجاهات[i]
        طول_خام = len(س)
        نص_مهيأ = ar(س) if _يحتوي_عربي(س) else س
        فراغ = max(عرض_السطر - طول_خام, 0)

        if اتجاه_السطر == "rjust":
            محتوى = " " * فراغ + نص_مهيأ
        elif اتجاه_السطر == "ljust":
            محتوى = نص_مهيأ + " " * فراغ
        else:  # center
            يسار = فراغ // 2
            يمين = فراغ - يسار
            محتوى = " " * يسار + نص_مهيأ + " " * يمين

        فراغ_اضافي = max(عرض_الصندوق - عرض_السطر - حشو * 2, 0)
        لون_هذا_السطر = (الوان_مخصصة[i] if الوان_مخصصة and الوان_مخصصة[i] else لون_نص)
        سطر_كامل = (
            لون_اطار + لون_خلفية + "║" + ل.إعادة +
            " " * حشو + لون_هذا_السطر + محتوى + ل.إعادة + " " * فراغ_اضافي +
            " " * حشو +
            لون_اطار + لون_خلفية + "║" + ل.إعادة
        )
        النتيجة.append(سطر_كامل)

    النتيجة.append(خط_سفلي)
    return "\n".join(النتيجة)


