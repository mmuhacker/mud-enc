# ============ توليد المفتاح من كلمة مرور ============
def اشتق_مفتاح(كلمة_المرور, الملح):
    kdf = PBKDF2HMAC(
        algorithm=hashes.SHA256(),
        length=32,
        salt=الملح,
        iterations=200000,
    )
    مفتاح_مشتق = base64.urlsafe_b64encode(kdf.derive(كلمة_المرور.encode()))
    return مفتاح_مشتق


# ============ توليد / تحميل ملف المفتاح ============
def ولّد_ملف_مفتاح(مسار_الملف):
    مفتاح_جديد = Fernet.generate_key()
    with open(مسار_الملف, "wb") as ملف_خرج:
        ملف_خرج.write(مفتاح_جديد)
    if not المنصة_ترمكس:
        try:
            os.chmod(مسار_الملف, 0o600)
        except Exception:
            pass
    return مفتاح_جديد


def حمّل_ملف_مفتاح(مسار_الملف):
    with open(مسار_الملف, "rb") as ملف_دخل:
        محتوى_المفتاح = ملف_دخل.read().strip()
    Fernet(محتوى_المفتاح)  # يتحقق من صحة المفتاح، يرمي استثناء إن كان غير صالح
    return محتوى_المفتاح


# ============ تشفير / فك تشفير نص ============
def شفّر_نص(النص_الأصلي, وضع_المفتاح, كلمة_المرور=None, مفتاح_الملف=None):
    if وضع_المفتاح == "password":
        الملح = os.urandom(16)
        المفتاح = اشتق_مفتاح(كلمة_المرور, الملح)
        اداة_التشفير = Fernet(المفتاح)
        الرمز_المشفر = اداة_التشفير.encrypt(النص_الأصلي.encode())
        الملح_مرمز = base64.urlsafe_b64encode(الملح).decode()
        return TAG_P + الملح_مرمز + ":" + الرمز_المشفر.decode()
    else:
        اداة_التشفير = Fernet(مفتاح_الملف)
        الرمز_المشفر = اداة_التشفير.encrypt(النص_الأصلي.encode())
        return TAG_K + الرمز_المشفر.decode()


def فك_تشفير_نص(النص_المشفر, وضع_المفتاح, كلمة_المرور=None, مفتاح_الملف=None):
    if النص_المشفر.startswith(TAG_P):
        if وضع_المفتاح != "password":
            raise ValueError("هذا النص مشفر بوضع (كلمة مرور)، بدّل وضع المفتاح الحالي")
        الباقي = النص_المشفر[len(TAG_P):]
        الملح_مرمز, الرمز = الباقي.split(":", 1)
        الملح = base64.urlsafe_b64decode(الملح_مرمز)
        المفتاح = اشتق_مفتاح(كلمة_المرور, الملح)
        اداة_التشفير = Fernet(المفتاح)
        النص_الاصلي = اداة_التشفير.decrypt(الرمز.encode())
        return النص_الاصلي.decode()
    elif النص_المشفر.startswith(TAG_K):
        if وضع_المفتاح != "keyfile":
            raise ValueError("هذا النص مشفر بوضع (ملف مفتاح)، بدّل وضع المفتاح الحالي")
        الرمز = النص_المشفر[len(TAG_K):]
        اداة_التشفير = Fernet(مفتاح_الملف)
        النص_الاصلي = اداة_التشفير.decrypt(الرمز.encode())
        return النص_الاصلي.decode()
    else:
        raise ValueError("هذا النص ليس مشفراً بصيغة أداة مدارك")


# ============ تشفير / فك تشفير ملف (أي نوع) ============
def شفّر_ملف(مسار_الملف, وضع_المفتاح, كلمة_المرور=None, مفتاح_الملف=None):
    with open(مسار_الملف, "rb") as ملف_دخل:
        البيانات = ملف_دخل.read()

    if وضع_المفتاح == "password":
        الملح = os.urandom(16)
        المفتاح = اشتق_مفتاح(كلمة_المرور, الملح)
        اداة_التشفير = Fernet(المفتاح)
        الرمز_المشفر = اداة_التشفير.encrypt(البيانات)
        الترويسة = MAGIC_P + base64.urlsafe_b64encode(الملح) + b":"
    else:
        اداة_التشفير = Fernet(مفتاح_الملف)
        الرمز_المشفر = اداة_التشفير.encrypt(البيانات)
        الترويسة = MAGIC_K

    مسار_الخرج = مسار_الملف + ".mud"
    with open(مسار_الخرج, "wb") as ملف_خرج:
        ملف_خرج.write(الترويسة + الرمز_المشفر)
    return مسار_الخرج


def فك_تشفير_ملف(مسار_الملف, وضع_المفتاح, كلمة_المرور=None, مفتاح_الملف=None):
    with open(مسار_الملف, "rb") as ملف_دخل:
        البيانات_الخام = ملف_دخل.read()

    if البيانات_الخام.startswith(MAGIC_P):
        if وضع_المفتاح != "password":
            raise ValueError("هذا الملف مشفر بوضع (كلمة مرور)، بدّل وضع المفتاح الحالي")
        الباقي = البيانات_الخام[len(MAGIC_P):]
        الملح_مرمز, الرمز = الباقي.split(b":", 1)
        الملح = base64.urlsafe_b64decode(الملح_مرمز)
        المفتاح = اشتق_مفتاح(كلمة_المرور, الملح)
        اداة_التشفير = Fernet(المفتاح)
    elif البيانات_الخام.startswith(MAGIC_K):
        if وضع_المفتاح != "keyfile":
            raise ValueError("هذا الملف مشفر بوضع (ملف مفتاح)، بدّل وضع المفتاح الحالي")
        الرمز = البيانات_الخام[len(MAGIC_K):]
        اداة_التشفير = Fernet(مفتاح_الملف)
    else:
        raise ValueError("هذا الملف ليس مشفراً بصيغة أداة مدارك")

    البيانات_الاصلية = اداة_التشفير.decrypt(الرمز)
    if مسار_الملف.endswith(".mud"):
        مسار_الخرج = مسار_الملف[:-4]
    else:
        مسار_الخرج = مسار_الملف + ".dec"
    with open(مسار_الخرج, "wb") as ملف_خرج:
        ملف_خرج.write(البيانات_الاصلية)
    return مسار_الخرج


